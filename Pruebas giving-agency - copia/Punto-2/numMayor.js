//Obtener el mayor número en un arreglo, como condición solo debes iterar el arreglo 1 vez.
// definimos variables
var array = [ 10, 25, 100, 8, 30, 200];

// definimos logica 
function numeroMayor(array){
    for(var i=0; i < array.length; i++ ){
        max = Math.max (...array)
        console.log(max);
        return 0;
    };
};
// llamada a la funcion y alert 
numeroMayor(array);
alert( "El numero mayor es " + max);



